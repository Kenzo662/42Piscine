int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	n;

	n = 0;
	sign = 1;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	n *= sign;
	return (n);
}
int main()
{
    char str[] = "-+56fkerejrje45786";
    int result = ft_atoi(str);
    printf("%d\n", result);
    return (0);
}
